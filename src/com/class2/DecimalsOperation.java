package com.class2;

public class DecimalsOperation {

	public static void main(String[] args) {

		double a = 10.5;
		double b = 5.5;

		System.out.println(a + b); // 16
		System.out.println(a - b); // 5
		System.out.println(a * b); // 57.75
		System.out.println(a / b); // 1.909

		double sum = a + b;
		double sub = a - b;
		double mult = a * b;
		double div = a / b;

		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);

		// The__ of 2 numbers __ and __ is equal to..
		System.out.println("The sum of 2 numbers 10.5 and 5.5 is " + sum + "");
		System.out.println("The subtraction of 2 numbers 10.5 and 5.5 is " + sub + "");
		System.out.println("The multiplication of 2 numbers 10.5 and 5.5 is " + mult + "");
		System.out.println("The division of 2 numbers 10.5 and 5.5 is " + div + "");


	}

}
